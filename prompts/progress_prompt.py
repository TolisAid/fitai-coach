# Prompt για προσαρμογή υπάρχοντος προγράμματος βάσει προόδου
def create_progress_prompt(
    selected_plan,
    current_weight,
    goal,
    fatigue_level,
    progress_feedback
):

    return f"""
Είσαι AI fitness coach και nutrition assistant.

Ο χρήστης έχει ήδη το παρακάτω πρόγραμμα:

{selected_plan}

Νέα δεδομένα χρήστη:
- Τρέχον βάρος: {current_weight}
- Στόχος: {goal}
- Επίπεδο κόπωσης: {fatigue_level}
- Feedback χρήστη: {progress_feedback}

Θέλω να προσαρμόσεις το υπάρχον πρόγραμμα, όχι να δημιουργήσεις άσχετο νέο πρόγραμμα.

# Ανάλυση Προόδου

Γράψε σύντομα:
- τι δείχνει η πρόοδος
- τι πρέπει να αλλάξει
- αν η κόπωση επηρεάζει το πρόγραμμα

# Προσαρμοσμένο Πρόγραμμα Γυμναστικής

Τροποποίησε το υπάρχον πρόγραμμα:
- κράτησε τη βασική δομή
- άλλαξε ασκήσεις μόνο όπου χρειάζεται
- προσάρμοσε sets/reps/cardio/recovery
- οι ασκήσεις να είναι στα Αγγλικά
- οι εξηγήσεις στα Ελληνικά

# Προσαρμοσμένο Πρόγραμμα Διατροφής

Τροποποίησε τη διατροφή:
- θερμίδες
- πρωτεΐνη
- υδατάνθρακες
- λιπαρά
- γεύματα

# Τι άλλαξε

Στο τέλος γράψε bullet list με τις βασικές αλλαγές που έγιναν.

Η απάντηση να είναι καθαρή και σε Markdown.
"""