# Συνάρτηση που δημιουργεί το prompt για το AI
def create_fitness_prompt(
    age,
    weight,
    height,
    goal,
    experience,
    gender,
    training_days,
    equipment,
    diet_type,
    injuries,
    bmi,
    bmi_category,
    calories,
    protein
):
    
    # Επιστρέφουμε το κείμενο που θα σταλεί στο AI
    return f"""
Είσαι personal trainer και nutrition expert.

Δημιούργησε:
1. Αναλυτικό πρόγραμμα γυμναστικής
2. Αναλυτική διατροφή

Στοιχεία χρήστη:
Ηλικία: {age}
Βάρος: {weight}
Ύψος: {height}
Στόχος: {goal}
Εμπειρία: {experience}
Φύλο: {gender}
Ημέρες προπόνησης: {training_days}
Εξοπλισμός: {equipment}
Τύπος διατροφής: {diet_type}
Τραυματισμοί: {injuries}
BMI: {bmi}
Κατηγορία BMI: {bmi_category}
Προτεινόμενες θερμίδες: {calories}
Προτεινόμενη πρωτεΐνη: {protein}g

Αν υπάρχουν τραυματισμοί ή προβλήματα υγείας:
- προσάρμοσε το πρόγραμμα ανάλογα
- απέφυγε επικίνδυνες ασκήσεις
- εξήγησε ποιες ασκήσεις αποφεύγονται
- πρότεινε ασφαλείς εναλλακτικές

Η απάντηση να είναι ΠΟΛΥ οργανωμένη.
Οι ασκήσεις να εμφανίζονται ΠΑΝΤΑ στα Αγγλικά.

Χρησιμοποίησε πραγματικές fitness ασκήσεις όπως:
- Bench Press
- Squat
- Deadlift
- Pull Ups
- Shoulder Press
- Leg Press

Απόφυγε λάθος ελληνικές μεταφράσεις ασκήσεων.

Το πρόγραμμα να μοιάζει με επαγγελματικό fitness πρόγραμμα γυμναστηρίου.

Χρησιμοποίησε:

# Πρόγραμμα Γυμναστικής

# Πρόγραμμα Διατροφής

Για κάθε ημέρα να υπάρχει:
- Όνομα ημέρας
- Ασκήσεις
- Sets
- Reps
- Διάρκεια

Για κάθε άσκηση να υπάρχει:
- όνομα άσκησης
- sets
- reps
- μικρή περιγραφή

Η διατροφή να είναι ΠΟΛΥ αναλυτική και να περιλαμβάνει:

# Πρόγραμμα Διατροφής

Για κάθε γεύμα να γράφεις:
- συγκεκριμένες τροφές
- ενδεικτικές ποσότητες σε γραμμάρια
- θερμίδες ανά γεύμα
- πρωτεΐνη ανά γεύμα
- υδατάνθρακες ανά γεύμα
- λιπαρά ανά γεύμα

Να υπάρχουν οι ενότητες:
- Πρωινό
- Δεκατιανό
- Μεσημεριανό
- Απογευματινό
- Βραδινό
- Προαιρετικό snack

Στο τέλος να υπάρχει:

# Ημερήσιο Σύνολο

με:
- συνολικές θερμίδες
- συνολική πρωτεΐνη
- συνολικούς υδατάνθρακες
- συνολικά λιπαρά
- 2-3 πρακτικές συμβουλές διατροφής

ΜΗΝ γράφεις απλά λίστα τροφών.
Να είναι σαν πραγματικό ημερήσιο διατροφικό πλάνο.

Να υπάρχουν κενά ανάμεσα στις ενότητες.

ΜΗΝ γράφεις όλα σε μία παράγραφο.
"""